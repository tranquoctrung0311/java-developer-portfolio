    private void lblImageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblImageMouseClicked
        // TODO add your handling code here:
        int selection = MessageDialog.showConfirmDialog(this, "Change profile picture?", "Confirm");
        if (selection == JOptionPane.YES_OPTION) {
            JFileChooser chooser = new JFileChooser();
            chooser.setFileFilter(new FileFilter() {
                @Override
                public boolean accept(File f) {
                    String extension = f.getName().toLowerCase();
                    boolean jpg = extension.endsWith(".jpg");
                    boolean png = extension.endsWith(".png");
                    if (f.isDirectory()) {
                        return true;
                    } else {
                        return (jpg || png);
                    }
                }

                @Override
                public String getDescription() {
                    return "Image File (*.jpg, *.png)";
                }
            });

            if (chooser.showOpenDialog(this) == JFileChooser.CANCEL_OPTION) {
                return;
            }

            File file = chooser.getSelectedFile();
            String path = file.getPath();
            try {
                if (dao.updateImage(path)) {

                }
            } catch (Exception e) {
                MessageDialog.showErrorDialog(this, e.getMessage(), "Error");
            }
            showImage(path);
        }
    }//GEN-LAST:event_lblImageMouseClicked

